SELECT
    c.coach_id,
    c.coach_name,
    c.coach_phone,
    c.coach_gender,
    c.coach_info,
    c.coach_price,
    c.create_date,
    c.update_at,
    GROUP_CONCAT(
        DISTINCT ct.code_desc
        ORDER BY ct.code_desc SEPARATOR '、'
    ) AS skills,
    ci.coach_img,
    g.gym_name AS gym
FROM
    Coaches c
    JOIN CoachSkills cs ON c.coach_id = cs.coach_id
    JOIN CommonType ct ON cs.commontype_id = ct.commontype_id
    JOIN CoachImgs ci ON c.coachImgs_id = ci.coachImgs_id
    JOIN Gyms g ON c.gym_id = g.gym_id
GROUP BY
    c.coach_id,
    g.gym_name,
    ci.coach_img;
